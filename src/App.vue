<template>
  <div id="app">
    <transition :name="transitionName">
      <router-view class="router-view"></router-view>
    </transition>
  </div>
</template>

<script>


export default {
  name: 'app',
  components: {
    
  },
  data(){
    return {
      transitionName:'vux-pop-in'
   }
  },
  
  mounted(){
    
  },
  watch:{
  $route(to, from) {
   if(to.meta.index > from.meta.index){
    this.transitionName = 'vux-pop-in';
   }else{
    this.transitionName = 'vux-pop-out';
   }
  }
 }
}
</script>

